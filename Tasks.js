Head1 = document.getElementById('T1B');
Content1 = document.getElementById('Task1');
Content1.style.display = 'none'


Head1.addEventListener("click", ()=>
    {
        if(Content1.style.display === 'none')
        {
            Content1.style.display = 'block';
        }
        else
        {
            Content1.style.display = 'none';
        }
    }
)


